function createCountryList(countriesList, countryList) {
	let innerElement = '';
	countriesList.forEach(country => {
		innerElement += `
		<li class = "country-list_item">${country}</li>
		`
		countryList.innerHTML = innerElement
	});
}

function filterList(countriesList, searchText) {
	return countriesList.filter(x => x.toLowerCase().includes(searchText.toLowerCase()))
}

inputCountry.addEventListener('input', () => {
	if(inputCountry !== '') {
		countryList.style.display = 'block';
		const filteredData = filterList(countriesList, inputCountry.value);
		createCountryList(filteredData, countryList)
		countryList.addEventListener('click', (e) => {
			const choiceCountry = e.target.innerText;
			inputCountry.value = choiceCountry;
			countryList.style.display = 'none'
		})
	}
})
inputCountry.onfocus = function() {
	countryList.style.display = 'none'
}



