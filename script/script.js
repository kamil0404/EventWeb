loadMore.addEventListener('click', showBlogItems)
let currentItem = 0
function showBlogItems() {
	let numShowItems = 3
	for(i = 0; i < numShowItems; i++) {
		if(currentItem >= 3) loadMore.style.display = 'none'
		const html = `
		<li class="item">
			<img src="${blogItems[i +currentItem].imgSrc}" alt="blog-img">
			<h3 class="item_title">${blogItems[i +currentItem].title}</h3>
			<p class="item_desc">${blogItems[i +currentItem].desc}</p>
			<div class="item_date">
				<span class="date">${blogItems[i +currentItem].autor.date}</span> -
				<span class="name-autor">${blogItems[i +currentItem].autor.name}</span>
			</div>
		</li>
		`
		blogEventItems.insertAdjacentHTML('beforeend', html)
	}
	currentItem =+ numShowItems
}


